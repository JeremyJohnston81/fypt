<h2>How does it work?</h2>
<p>In Texas there is a "Uniform and Equal" appraisal law - Section 41.43 of the Texas Property Tax Code. This law states that your home must be appraised equal to the median price per sq ft of a reasonable amount of comparables in your neighborhood (5-10).</p>
<p>Homes are graded by the appraisal district on 3 major factors</p>
<ul>
    <li>CAD (Cost & Design): This accounts for differences in what is typical for the neighborhood. For example, new remodels or a new home in an older neighborhood, etc. Not all properties will have a CAD value.</li>
    <li>CDU (Condition, Desirability, and Utility): This is also known as the "Percent Good" factor. It accounts for age and depreciation.</li>
    <li>GRD (Grade): This is a score assigned by the appraisal district to account for the quality of workmanship and materials</li>
</ul>
<p>I find all the comps in your neighborhood with similar characteristics. Adjustments are made to each comp for the difference in CAD, CDU, and GRD.  I then take the median price/sqft and apply that to your home to get the proper appraisal value.</p>

